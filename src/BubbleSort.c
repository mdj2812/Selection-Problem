/*******************************************************************************
 * Internal function declaration
 ******************************************************************************/
static void swap(uint32_t *xp, uint32_t *yp);

/*******************************************************************************
 * Variables
 ******************************************************************************/

/*******************************************************************************
 * Public function
 ******************************************************************************/
void BubbleSort_Asc(uint32_t* nt, uint32_t len) {
	uint32_t i, j;

	for (i = 0; i < len - 1; i++) {
		// Last i elements are already in place
		for (j = 0; j < len - i - 1; j++) {
			if (nt[j] > nt[j + 1]) {
				swap(&nt[j], &nt[j + 1]);
			}
		}
	}
}

void BubbleSort_Desc(uint32_t* nt, uint32_t len) {
	uint32_t i, j;

	for (i = 0; i < len - 1; i++) {
		// Last i elements are already in place
		for (j = 0; j < len - i - 1; j++) {
			if (nt[j] < nt[j + 1]) {
				swap(&nt[j], &nt[j + 1]);
			}
		}
	}
}

/*******************************************************************************
 * Internal function
 ******************************************************************************/
void swap(uint32_t *xp, uint32_t *yp) {
	uint32_t temp = *xp;

	*xp = *yp;
	*yp = temp;
}
